# ---------------------------------------------------------------------------
# Направления взгляда (Yaw) в Minecraft
# ---------------------------------------------------------------------------
DIRECTIONS = {
    'юг':    0.0,
    'запад': 90.0,
    'север': 180.0,
    'восток': -90.0,
    'south': 0.0,
    'west':  90.0,
    'north': 180.0,
    'east':  -90.0,
}


def _input_int(prompt: str) -> int:
    """
    Запрашивает целое число с повторным вводом при ошибке.

    Аргументы:
        prompt: текст подсказки

    Возвращает:
        Введённое целое число
    """
    while True:
        try:
            return int(input(prompt))
        except ValueError:
            print("    ⚠ Ошибка: введите целое число!")


def _input_float(prompt: str, default: float | None = None) -> float:
    """
    Запрашивает число с плавающей точкой с поддержкой значения по умолчанию.

    Аргументы:
        prompt: текст подсказки
        default: значение по умолчанию (None = обязательный ввод)

    Возвращает:
        Введённое число (или значение по умолчанию)
    """
    while True:
        raw = input(prompt).strip()
        if not raw and default is not None:
            return default
        try:
            return float(raw)
        except ValueError:
            print("    ⚠ Ошибка: введите число!")


def _input_coordinates(label: str) -> tuple[int, int, int]:
    """
    Запрашивает три координаты (X, Y, Z).

    Поддерживает два формата ввода:
        1. По одной: X, затем Y, затем Z (каждое с Enter)
        2. Все сразу через пробел: "100 64 200"

    Аргументы:
        label: метка для отображения (например, "Точка 1")

    Возвращает:
        Кортеж (x, y, z)
    """
    print(f"\n  📍 {label}")
    print(f"     (введите X Y Z через пробел, или по одному)")

    raw = input(f"     > ").strip()

    # Пробуем распарсить все три координаты из одной строки
    parts = raw.replace(',', ' ').split()
    if len(parts) >= 3:
        try:
            x = int(parts[0])
            y = int(parts[1])
            z = int(parts[2])
            return x, y, z
        except ValueError:
            pass

    # Если не получилось — запрашиваем по одному
    # Первое введённое число считаем X
    try:
        x = int(raw)
    except ValueError:
        x = _input_int("     X: ")

    y = _input_int("     Y: ")
    z = _input_int("     Z: ")

    return x, y, z


def get_cuboid_coordinates() -> tuple[tuple[int, int, int], tuple[int, int, int]]:
    """
    Запрашивает координаты двух противоположных точек кубоида.

    Возвращает:
        Кортеж из двух кортежей: ((x1, y1, z1), (x2, y2, z2))
    """
    print("\n" + "=" * 60)
    print("  КООРДИНАТЫ ОБЛАСТИ ДЛЯ ОЧИСТКИ")
    print("=" * 60)
    print("  Введите координаты двух противоположных углов кубоида.")
    print("  Подсказка: в Minecraft нажмите F3 для отображения координат.")

    point1 = _input_coordinates("Точка 1 (первый угол)")
    point2 = _input_coordinates("Точка 2 (противоположный угол)")

    # Вычисляем размер области
    size_x = abs(point2[0] - point1[0]) + 1
    size_y = abs(point2[1] - point1[1]) + 1
    size_z = abs(point2[2] - point1[2]) + 1
    total = size_x * size_y * size_z

    print(f"\n  ✅ Область задана:")
    print(f"     Размер: {size_x} × {size_y} × {size_z} = {total} блоков")

    return point1, point2


def get_player_position() -> tuple[float, float, float]:
    """
    Запрашивает текущую позицию персонажа.

    Возвращает:
        Кортеж (x, y, z) — координаты ног персонажа
    """
    print("\n" + "=" * 60)
    print("  ТЕКУЩАЯ ПОЗИЦИЯ ПЕРСОНАЖА")
    print("=" * 60)
    print("  Введите координаты, на которых сейчас стоит персонаж.")
    print("  ⚠ Важно: используйте координаты из F3-экрана (Block или Feet).")

    raw = input(f"\n  📍 Позиция (X Y Z через пробел): ").strip()

    parts = raw.replace(',', ' ').split()
    if len(parts) >= 3:
        try:
            x = float(parts[0])
            y = float(parts[1])
            z = float(parts[2])
            return x, y, z
        except ValueError:
            pass

    try:
        x = float(raw)
    except ValueError:
        x = _input_float("     X: ")

    y = _input_float("     Y: ")
    z = _input_float("     Z: ")

    return x, y, z


def get_player_direction() -> float:
    """
    Запрашивает направление взгляда персонажа (Yaw).

    Пользователь может ввести:
        - Название стороны света: юг, север, запад, восток
        - Числовое значение Yaw из F3-экрана

    Возвращает:
        Угол Yaw в градусах
    """
    print("\n  🧭 Направление взгляда:")
    print("     Варианты: юг (0°), запад (90°), север (180°), восток (-90°)")
    print("     Или введите точное значение Yaw из F3-экрана.")

    while True:
        raw = input("     > ").strip().lower()

        # Проверяем название стороны света
        if raw in DIRECTIONS:
            yaw = DIRECTIONS[raw]
            print(f"     ✅ Направление: {raw} (Yaw = {yaw}°)")
            return yaw

        # Пробуем как число
        try:
            yaw = float(raw)
            print(f"     ✅ Yaw = {yaw}°")
            return yaw
        except ValueError:
            print("     ⚠ Ошибка: введите сторону света или число!")


def get_settings() -> tuple[float, float]:
    """
    Запрашивает дополнительные настройки бота.

    Возвращает:
        Кортеж (mouse_sensitivity, block_break_time)
    """
    print("\n" + "=" * 60)
    print("  НАСТРОЙКИ")
    print("=" * 60)

    # --- Чувствительность мыши ---
    print("\n  🖱 Чувствительность мыши в Minecraft:")
    print("     Значение из настроек игры (0% - 200%, стандарт = 100%)")
    sensitivity_pct = _input_float(
        "     Чувствительность [100]: ",
        default=100.0
    )
    # Переводим проценты (0-200) во внутреннее значение (0.0-1.0)
    mouse_sensitivity = sensitivity_pct / 200.0
    mouse_sensitivity = max(0.0, min(1.0, mouse_sensitivity))

    # --- Время копания блока ---
    print("\n  ⛏ Время разрушения блока (секунды):")
    print("     Зависит от инструмента и типа блока.")
    print("     Примеры: камень + алмазная кирка ≈ 0.3 сек")
    print("              земля + лопата ≈ 0.15 сек")
    print("              обсидиан + алмазная кирка ≈ 9.4 сек")
    block_break_time = _input_float(
        "     Время копания [0.5]: ",
        default=0.5
    )
    block_break_time = max(0.05, block_break_time)  # Минимум 50 мс

    return mouse_sensitivity, block_break_time


